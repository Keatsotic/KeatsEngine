Map::Map()
{
	dirt = TextureManager::LoadTexture("assets/Sprites/Tilesets/dirt.png");
	grass = TextureManager::LoadTexture("assets/Sprites/Tilesets/grass.png");
	water = TextureManager::LoadTexture("assets/Sprites/Tilesets/water.png");

	LoadMap(lvl1);

	src.x = src.y = 0;
	src.w = src.h = tileSize;
	dest.w = dest.h = tileSize;
	dest.x = dest.y = 0;
}

Map::~Map()
{}

void 
Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void
Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];
			dest.x = column * tileSize;
			dest.y = row * tileSize;
			//hmmm
			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;
			}
		}
	}
}